void Solve()
{
    int n;cin >> n;
    vector<int>v(n);
    for(auto & i : v)cin >> i;

    vector<int>dp(n + 5);

    dp[0] = 0;                  //base case
    dp[1] = abs(v[0] - v[1]);//base case


    //      dp[i] state : Minimum cost for frog to reach position i
    for (int i = 2; i < n; ++i)
        dp[i] = min(abs(v[i - 2] - v[i]) + dp[i - 2], abs(v[i - 1] - v[i]) + dp[i - 1]);    //transitions

    cout << dp[--n];

}

