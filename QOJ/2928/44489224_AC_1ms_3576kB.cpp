void Solve()
{
    int n, k, cnt = 0, kk;cin >> n >> k, kk = k;

    while (n--)
    {
        string s;cin >> s;

        int tmp = ((s.size() == 1) ? s[0] - '0' : s[0] - '0' + 1);
        if (k - tmp >= 0)
            k -= tmp;
        else
            cnt++, k = kk - tmp;
    }
    cout << cnt;
}

